package OOPFirstPart;

public class SingleInheritanceParent {
    int number;
    public void addition(int number1, int number2){
        number = number1+number2;
        System.out.println("Summation is: "+ number);
    }
    public void subtraction(int number1, int number2){
        number = number1-number2;
        System.out.println("subtraction is: "+number);
    }

}
